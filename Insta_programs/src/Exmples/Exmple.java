package Exmples;

public class Exmple {
	public static void main(String[] args) {
		int dayofweek=7;
		switch (dayofweek) {

		case 1:
			System.out.println("1:monday");
			break;
		case 2:
			System.out.println("2:tue");
			break;
		case 3:
			System.out.println("3:wes");
			break;
		case 4:
			System.out.println("4:thus");
			break;
		case 5:
			System.out.println("5:fir");
			break;
		case 6:
			System.out.println("6:sat");
			break;
		case 7:
			System.out.println("7:sun");
			break;
		default:
			break;
		}
	}
}
